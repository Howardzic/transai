/* =============================================
   CORE FUNCTIONS
   ============================================= */
function getLang() { return localStorage.getItem('transai_lang') || 'zh-TW'; }

window._t = function(key) {
  const lang = getLang();
  return (TRANSLATIONS[lang] && TRANSLATIONS[lang][key] != null ? TRANSLATIONS[lang][key] : null)
    ?? (TRANSLATIONS['zh-TW'][key] ?? key);
};

function applyLang(lang) {
  localStorage.setItem('transai_lang', lang);
  document.documentElement.lang = lang;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const val = window._t(el.getAttribute('data-i18n'));
    if (val != null) el.textContent = val;
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    el.placeholder = window._t(el.getAttribute('data-i18n-ph'));
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    el.title = window._t(el.getAttribute('data-i18n-title'));
  });
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  window.dispatchEvent(new CustomEvent('langchange', { detail: { lang } }));
}

